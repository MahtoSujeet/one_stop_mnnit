class LostFoundItem {
  final String name;
  final String description;
  final String contact;
  final String image;
  final String location;
  final DateTime date;
  final bool isLost;

  LostFoundItem({
    required this.name,
    required this.description,
    required this.contact,
    required this.image,
    required this.location,
    required this.date,
    required this.isLost,
  });
}
